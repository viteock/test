package day03_escape_sequence;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Let's buy  \"coffee\" and \"tea\"");
        System.out.println("java\nintelij");
        System.out.println("java\\nintelij");
        System.out.println("java\tintelij");
        System.out.println("   ^  ");
        System.out.println(" /   \\");
        System.out.println(" |   |");
        System.out.println("-------");
        System.out.println();
        System.out.println();
    }


}
