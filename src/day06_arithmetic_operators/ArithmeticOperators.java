package day06_arithmetic_operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 23;
        int b = 4;

        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a-b);
        System.out.println(a%b);
        System.out.println(2 * -2);
    }
}
