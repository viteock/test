package day07_arithmetic_operators_casting;

public class DivisionProblem {
    public static void main(String[] args) {
        double d1 = 12;
        double d2 = 5;
        System.out.println(d1/d2);

        // int / double  =>good
        int count = 100;
        double dCount = 30.0;
        System.out.println(count / dCount);
    }

}
